def is_similar_to_training(x_raw: np.ndarray, mu: np.ndarray, sigma: np.ndarray, threshold=ANFIS_SIMILARITY_THRESHOLD):
    # Mahalanobis-like distance using diag(sigma)
    diff = (x_raw - mu)
    # avoid zero sigma
    sig = np.copy(sigma); sig[sig==0] = 1.0
    dist = np.sqrt(np.sum((diff / sig) ** 2))
    return float(dist), (dist <= threshold)

