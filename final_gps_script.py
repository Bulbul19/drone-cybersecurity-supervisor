def load_anfis_model(model_path: str, meta_path: str, device="cpu"):
    # read meta
    meta = {}
    if Path(meta_path).exists():
        with open(meta_path, "r") as f:
            meta = json.load(f)
    else:
        print(f"[WARN] meta file {meta_path} not found. continuing with defaults.")

    # try find n_mfs
    n_mfs = meta.get("n_mfs")
    # if not in meta, try infer from checkpoint keys
    if n_mfs is None and Path(model_path).exists():
        # load state keys only
        sd = torch.load(model_path, map_location="cpu")
        # count mf_x1.* keys
        keys = list(sd.keys())
        # pattern 'mf_x1.{i}.mean' or 'mf_x1.{i}.mean' depending on training
        count = 0
        for k in keys:
            if k.startswith("mf_x1.") and ".mean" in k:
                # extract index
                count += 1
        if count > 0:
            n_mfs = count
    if n_mfs is None:
        n_mfs = meta.get("mu") and 3 or 3  # fallback 3

    print(f"[INFO] Using n_mfs = {n_mfs}")

    # construct model and attempt to load
    model = ANFIS_3in_1out(n_mfs=n_mfs)
    try:
        state = torch.load(model_path, map_location=device)
        # if it's state_dict, try to load
        if isinstance(state, dict) and all(k.startswith("mf_x1") or k.startswith("mf_x2") or k.startswith("mf_x3") or k.startswith("consequents") for k in state.keys()):
            model.load_state_dict(state)
        else:
            # maybe saved model.state_dict() or entire module - try both
            try:
                model.load_state_dict(state)
            except Exception:
                # maybe user saved a model object, attempt to load and extract state_dict
                temp = state
                if hasattr(temp, "state_dict"):
                    sd = temp.state_dict()
                    model.load_state_dict(sd)
                else:
                    raise
        print("[INFO] ANFIS model loaded.")
    except Exception as e:
        print("[ERROR] Could not load ANFIS model cleanly:", e)
        return None, meta

    return model, meta

